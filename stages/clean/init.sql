create sequence lote_seq;

create table lote (
  loteSeq int NOT NULL DEFAULT NEXTVAL ('lote_seq'),
  source varchar(500),
  file_size varchar(25),
  rows varchar(40),
  loteStartProcessTimestamp timestamp(0),
  loteEndProcessTimestamp timestamp(0),
  loteId varchar(40),
  CONSTRAINT loteid_unique UNIQUE (loteId),
  PRIMARY KEY (loteSeq)
);


